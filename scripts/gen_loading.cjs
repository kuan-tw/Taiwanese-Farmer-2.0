let currentLangIndex = -1;
let newLines = [];
let lines = file.split('\n');
let i = 0;

while(i < lines.length) {
  const line = lines[i];
  if (line.match(/^  [a-z]{2}: \{/)) {
    currentLangIndex++;
  }
  
  if (line.includes('loading: {') && currentLangIndex !== -1) {
    const tData = loadingData[languages[currentLangIndex]];
    
    // We will generate the array strings directly
    const stringifyArray = (arr) => `[\n${arr.map(s => `        '${s.replace(/'/g, "\\'")}'`).join(',\n')}\n      ]`;
    
    newLines.push(`    loading: {`);
    newLines.push(`      planting: ${stringifyArray(tData.planting)},`);
    newLines.push(`      heading_to: ${stringifyArray(tData.heading_to)},`);
    newLines.push(`      epidemic: ${stringifyArray(tData.epidemic)},`);
    newLines.push(`      default: ${stringifyArray(tData.default)}`);
    newLines.push(`    },`);
    
    // Skip old loading block. We will just keep iterating until we hit }, which is the end of the loading block
    let braces = 1;
    i++;
    while(i < lines.length && braces > 0) {
      if (lines[i].includes('{')) braces++;
      if (lines[i].includes('}')) braces--;
      i++;
    }
    continue;
  }
  
  newLines.push(line);
  i++;
}

fs.writeFileSync('src/utils/translation.ts', newLines.join('\n'));

export const getTranslation = (language: string, path: string) => {
  const keys = path.split('.');
  let current: any = translations[language as keyof typeof translations];
  
  for (const key of keys) {
    if (current && typeof current === 'object') {
      current = current[key];
    } else {
      return path;
    }
  }
  
  return current || path;
};